     *
     @var array
     */
    protected $casts = [
    'id' => 'integer',
    'object_type' => 'string',
    'object_id' => 'integer',
    'subscription_id' => 'integer',
    'created_at' => 'datetime',
    'updated_at' => 'datetime',
